def aumentar(número):
    porcentode10 = (número * 10) / 100
    print(f'O número {número} com o aumento de 10% é igual à {número + porcentode10}.')


def diminuir(número):
    porcentode20 = (número * 20) / 100
    print(f'O número {número} com o desconto de 20% é igual à {número - porcentode20}.')


def dobro(número):
    print(f'O dobro do número {número} é igual à {número * 2}.')


def metade(número):
    print(f'A metade do número {número} é igual à {número / 2}.')